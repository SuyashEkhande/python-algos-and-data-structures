#Basic Operations and Arithmetic Operators

a = 16
b = 13

# Addition
add_result = a + b
print(f'{a} + {b} = {add_result}')

# Subtraction
sub_result = a - b
print(f'{a} - {b} = {sub_result}')

# Multiplication
mul_result = a * b
print(f'{a} * {b} = {mul_result}')

# Division
div_result = a / b
print(f'{a} / {b} = {div_result}')

# Modulo
mod_result = a % 2
print(f'{a} % 2 = {mod_result}')

# Floor Division
floor_div_result = a // b
print(f'{a} // {b} = {floor_div_result}')

# Exponentiation
exp_result = a ** b
print(f'{a} ** {b} = {exp_result}')

# Comparison Operators

# Equality
equal_result = a == b
print(f'{a} == {b} → {equal_result}')

# Not Equal
not_equal_result = a != b
print(f'{a} != {b} → {not_equal_result}')

# Greater Than
greater_than_result = a > b
print(f'{a} > {b} → {greater_than_result}')

# Less Than
less_than_result = a < b
print(f'{a} < {b} → {less_than_result}')

# Greater Than or Equal To
greater_or_equal_result = a >= b
print(f'{a} >= {b} → {greater_or_equal_result}')

# Less Than or Equal To
less_or_equal_result = a <= b
print(f'{a} <= {b} → {less_or_equal_result}')


# Logical Operators

x = True
y = False

# AND
and_result = x and y
print(f'{x} and {y} → {and_result}')

# OR
or_result = x or y
print(f'{x} or {y} → {or_result}')

# NOT
not_result = not x
print(f'not {x} → {not_result}')


